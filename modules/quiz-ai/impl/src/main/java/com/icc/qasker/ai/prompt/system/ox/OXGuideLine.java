package com.icc.qasker.ai.prompt.system.ox;

import lombok.AccessLevel;
import lombok.NoArgsConstructor;

@NoArgsConstructor(access = AccessLevel.PRIVATE)
public class OXGuideLine {

  public static final String content =
      """
      > **CRITICAL RULE**: 강의노트에 명시된 내용만 출제 근거로 사용하세요

      ## 핵심 설계 규칙

      ### 마크다운 서식 규칙
      - content, explanation, quizExplanation 필드에서 **비교·분류·대응 관계**가 있으면 마크다운 테이블로 정리
      - 테이블 예: 개념 비교표, 조건별 동작 정리, 용어-정의 매핑, 장단점 대비 등
      - 테이블은 2열 이상, 헤더 행 포함, 구분선(`|---|---|`) 사용

      ### content 규칙 (questions[].content — 문항 줄기, 단일 진술문)
      - **단일 진술 원칙**: 하나의 참/거짓 판단만 포함, 한 번 읽어서 의미가 파악되는 명료한 구문 사용
      - 핵심 개념을 원문에서 재구성하되, 조건/맥락을 반드시 포함
      - 절대어('항상', '절대', '모든')는 참/거짓 판별의 핵심 조건인 경우에만 사용
      - 이해~적용(Understand~Apply) — 개념의 의미를 설명·분류하거나 새로운 상황에 적용하는 진술
        - 활용 동사: 설명하다, 분류하다, 예측하다, 적용하다
        - 패턴: "~할 때 ~가 발생한다", "~는 ~에 해당한다", "~조건에서 ~를 사용할 수 있다"
      - 분석(Analyze) — 2개 이상 개념의 관계·인과·구조를 하나의 조건문으로 압축한 진술
        - 활용 동사: 비교하다, 구분하다, 원인을 파악하다, 관계를 추론하다
        - 패턴: "~이면 ~이다", "~가 ~에 영향을 미치는 이유는 ~때문이다"

      ### selections 규칙 (questions[].selections — 선택지)
      - **정답 분포 균형**: 여러 문항 시 O/X 정답을 균등 배분
      - **문항 독립성**: 각 문항을 독립적으로 풀 수 있도록 설계

      ### 오답 유형 (거짓 진술 변조 전략)
      거짓 진술 설계 시 아래 유형을 지정하고, explanation 첫 줄에 `[유형명]`을 표기하세요.
      - **사실 변조형**: 핵심 용어/수치를 유사한 것으로 교체
      - **관계 변조형**: 개념 간 관계(인과, 포함)를 뒤바꿈
      - **조건 변조형**: 적용 조건/범위를 미세 변경

      **매력도 검증**: "이를 참이라고 판단할 학습자는 어떤 오개념을 가지고 있는가?"를 각 거짓 진술에 명시하세요.

      ---

      ## 난이도 제어

      - 이해~적용(Understand~Apply): 단일 개념 조건 1개로 참/거짓 판단, 예상 정답률 70~80%
      - 분석(Analyze): 2개 이상 개념의 관계/인과를 **하나의 조건문으로 압축**하여 판단, 예상 정답률 50~65%
      - 배분: 1문항→이해~적용 또는 분석, 2문항→이해~적용+분석, 3문항+→균등

      ---

      ## 해설 구조

      **선택지별 해설** (selections[].explanation):

      정답 선택지 (correct=true인 O 또는 X):
      ```
      **[정답 추론]**
      {근거와 추론 경로 — 참(O) 진술이 정답인 경우에도 '왜 참인가' 근거를 인용}
      - 근거: [강의노트 섹션명] > "핵심 문장 인용"
      ```

      오답 선택지 (correct=false인 O 또는 X):
      ```
      [오답 유형: 사실 변조형/관계 변조형/조건 변조형]
      - **진단**: {오개념 진단}
      - **교정**: {정답과의 핵심 차이점}
      - **스스로 점검**: {오류 인식 유도 개방형 질문}
      - 복습: {강의노트 섹션명}
      ```

      **문항 전체 해설** (questions[].quizExplanation):
      ```
      **[자기 점검]** (난이도: 주제)
      {메타인지 질문 — 이해~적용: 유사 개념 비교, 분석: 맥락 전이 성찰}

      **[심화 학습]**
      {복습 범위 + 다음 Bloom's 수준 학습 경로}
      ```

      ---

      ## 참고 예시

      > 가상 강의노트 전제. 실제 출력 시 강의노트의 실제 섹션명 사용. selections[].content = "O" 또는 "X"만.

      ```json
      {
        "questions": [{
          "content": "TCP의 **3-way handshake**에서 클라이언트는 서버의 `SYN-ACK`를 수신한 후에야 `ACK`를 전송하고 데이터 전송을 시작할 수 있다.",
          "quizExplanation": "**[자기 점검]** (이해·적용: 유사 개념 비교)\\n3-way handshake(연결 수립)와 4-way handshake(연결 종료)의 단계 차이를 비교할 수 있나요?\\n\\n**[심화 학습]**\\nSYN Flooding 공격의 원인과 대응을 분석해 보세요. [TCP 연결 수립] 참조.",
          "selections": [
            {
              "content": "O",
              "correct": true,
              "explanation": "**[정답 추론]**\\n3-way handshake는 SYN → SYN-ACK → ACK 완료 후 ESTABLISHED 상태로 전이되어야 데이터 전송이 가능합니다.\\n- 근거: [TCP 연결 수립] > \\"3-way handshake의 세 단계가 모두 완료되어야 연결이 성립한다\\""
            },
            {
              "content": "X",
              "correct": false,
              "explanation": "[사실 변조형]\\n- **진단**: SYN 전송 후 SYN-ACK 없이 바로 데이터 전송 가능하다는 오개념\\n- **교정**: 세 단계 모두 완료해야 ESTABLISHED 상태로 전이됨\\n- **스스로 점검**: SYN-ACK를 받지 못한 상태에서 클라이언트가 할 수 있는 동작은?\\n- 복습: [TCP 연결 수립]"
            }
          ]
        }]
      }
      ```

      출력 전 각 content·explanation·quizExplanation 필드를 검토하세요.
      1. \\n 줄바꿈으로 논리적 단락이 구분되어 있는가?
      2. **굵게**, `코드`, **테이블(비교·분류 시 필수)**, > 인용 중 2가지 이상 사용했는가?
      """;
}
